    const tokenMatch = response.match(/(\d+\.?\d*)% token reduction/);
    
    if (speedMatch) console.log(`   Speed Improvement: ${speedMatch[1]}x`);
    if (tokenMatch) console.log(`   Token Reduction: ${tokenMatch[1]}%`);
